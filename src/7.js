// Add two numbers together using the plus operator (+)
let x = 5;
let y = 3;
console.log(x + y); // Output: 8

// Subtract one number from another using the minus (-) operator
let x = 10;
let y = 5;
console.log(x - y); // Output: 5

// Multiply two numbers together using the multiplication (*) operator
let x = 2;
let y = 3;
console.log(x * y); // Output: 6

// Divide one number by another using the division (/) operator
let x = 10;
let y = 5;
console.log(x / y); // Output: 2

// Use a modulo operator to find the remainder of a division operation
let x = 10;
let y = 3;
console.log(x % y); // Output: 1

// Use an increment operator to add 1 to a variable's value
let x = 5;
x++;
console.log(x); // Output: 6

// Use a decrement operator to subtract 1 from a variable's value
let x = 5;
x--;
console.log(x); // Output: 4
